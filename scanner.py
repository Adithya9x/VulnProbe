def scan_port(target, port):
    try:
        sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        sock.settimeout(1)
        result = sock.connect_ex((target, port))
        sock.close()
        if result == 0:
            return True
        else:
            return False
    except:
        return False

def run_scanner(target, start_port, end_port):
    ip = resolve_target(target)

    print("-" * 50)
    print(f"  VulnProbe - Network Vulnerability Scanner")
    print(f"  Target   : {target} ({ip})")
    print(f"  Ports    : {start_port} to {end_port}")
    print(f"  Started  : {datetime.datetime.now()}")
    print("-" * 50)

    open_ports = []
    scan_results = []
    start_time = datetime.datetime.now()

    for port in range(start_port, end_port + 1):
        print(f"  Scanning port {port}...", end="\r")
        if scan_port(ip, port):
            service = get_service(port)
            nmap_info = nmap_scan(ip, port)
            
            result_entry = {"port": port, "service": service}
            
            if nmap_info:
                product = nmap_info['product']
                version = nmap_info['version']
                print(f"  [OPEN] Port {port:5} --> {service:15} | {product} {version}")
                result_entry["product"] = product
                result_entry["version"] = version
                
                if product and version:
                    print(f"         Checking CVE database...")
                    cves = check_cve(product, version)
                    result_entry["cves"] = cves
                    if cves:
                        for cve in cves:
                            print(f"         [VULNERABLE] {cve['id']} - {cve['severity']} - {cve['description']}...")
                    else:
                        print(f"         No known CVEs found.")
            else:
                print(f"  [OPEN] Port {port:5} --> {service:20}")
            
            open_ports.append((port, service))
            scan_results.append(result_entry)

    print("-" * 50)
    print(f"  Scan complete. {len(open_ports)} open port(s) found.")
    print("-" * 50)
    
    if scan_results:
        generate_report(target, ip, scan_results, start_time)
    
    return open_ports

target = input("Enter target IP or hostname: ")
start_port = int(input("Enter start port: "))
end_port = int(input("Enter end port: "))

run_scanner(target, start_port, end_port)