import socket
import datetime
import nmap
import requests
import os
import webbrowser

def resolve_target(target):
    try:
        ip = socket.gethostbyname(target)
        return ip
    except:
        print("[-] Invalid target. Could not resolve hostname.")
        exit()

def get_service(port):
    try:
        service = socket.getservbyport(port)
        return service
    except:
        return "Unknown Service"

def nmap_scan(target, port):
    try:
        nm = nmap.PortScanner()
        nm.scan(target, str(port), arguments='-sV')
        state = nm[target]['tcp'][port]['state']
        service = nm[target]['tcp'][port]['name']
        version = nm[target]['tcp'][port]['version']
        product = nm[target]['tcp'][port]['product']
        return {
            'state': state,
            'service': service,
            'product': product,
            'version': version
        }
    except:
        return None
def check_cve(product, version):
    try:
        query = f"{product} {version}"
        url = "https://services.nvd.nist.gov/rest/json/cves/2.0"
        params = {
            "keywordSearch": query,
            "resultsPerPage": 5
        }
        response = requests.get(url, params=params, timeout=10)
        data = response.json()

        cve_list = []
        if "vulnerabilities" in data:
            for item in data["vulnerabilities"]:
                cve_id = item["cve"]["id"]
                description = item["cve"]["descriptions"][0]["value"]
                
                severity = "UNKNOWN"
                if "metrics" in item["cve"]:
                    if "cvssMetricV31" in item["cve"]["metrics"]:
                        severity = item["cve"]["metrics"]["cvssMetricV31"][0]["cvssData"]["baseSeverity"]
                    elif "cvssMetricV2" in item["cve"]["metrics"]:
                        severity = item["cve"]["metrics"]["cvssMetricV2"][0]["baseSeverity"]
                
                cve_list.append({
                    "id": cve_id,
                    "description": description[:100],
                    "severity": severity
                })
        return cve_list
    except Exception as e:
        return []
    
